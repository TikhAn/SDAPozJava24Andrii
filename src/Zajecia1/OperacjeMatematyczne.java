package Zajecia1;

public class OperacjeMatematyczne {
    public static void main (String[] args) {
        int a = 10;
        int b = 25;
        double wynik = a / b;

        System.out.println(wynik);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(wynik);
    }

}
